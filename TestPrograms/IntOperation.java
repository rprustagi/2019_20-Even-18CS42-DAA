public class  IntOperation {
   public static void main(String[] args) {
      double dd = Math.sqrt(-1);
      double ee = dd;
      System.out.println("dd=" + (dd) + " ee=" + (ee));
      System.out.println("-42 / 5=" + (-42 / 5) + ", floor(-42.0/5)=" + (Math.floor(-42.0/5)));
      System.out.println("42 / -5=" + (42 / -5));
      System.out.println("-42 % 5=" + (-42 % 5));
      System.out.println("42 % -5=" + (42 % -5));
      System.out.println("4^4=" + (4 ^ 4));
      System.out.println("4^8=" + (4 ^ 8));
      System.out.println("10^6=" + (10 ^ 6));


   }
}
